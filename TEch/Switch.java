class Switch {
    public static void main(String[] args) {

        char ch = 's';
        switch (ch) {
            case 'u':
                System.out.println("the number is 10");
                break;
            case 's':
                System.out.println("the number is 20");
                break;
            case 'm':
                System.out.println("the number is 300");
                break;
        }

    }
}